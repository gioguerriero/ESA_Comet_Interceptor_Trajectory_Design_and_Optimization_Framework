function write_python_inputs(out_cr3bp, c_const, filename)
% write_python_inputs  Export the trajectory data to a Python-readable .txt.
% All states are given in the non-dimensional CR3BP synodic frame.
%
% Inputs:
%   out_cr3bp - variables_organizer output (needs flyby.vinf_in/out,
%               moon_state and epoch to reconstruct the flyby state)
%   c_const   - constants struct with mu, Tstar and muMoon (or G+mMoon), rMoon
%   filename  - (optional) output file path (default 'python_inputs.txt')
%
% Outputs:
%   (none) - writes the text file to disk

if nargin < 3
    filename = 'python_inputs.txt';
end

mu    = c_const.mu;
Tstar = c_const.Tstar;

m      = 0.1;
T_halo = 3.083023392967281;   % [adim]

opt = odeset('AbsTol', 1e-13, 'RelTol', 1e-13);   % used only for the pre-injection back-propagation

%% ===== State after the injection maneuver =====
state_after_inj    = out_cr3bp.departure.state.syn;          % 1×6
state_after_inj(4:6) = state_after_inj(4:6) + out_cr3bp.injection.syn_adim;

%% ===== State m*T_halo before injection (back-propagated from departure, no dv) =====
dt_pre_inj = -m * T_halo;   % non-dimensional time, negative = backward
[~, S_pre] = ode45(@(t,S) CR3BP(t, S, mu), [0, dt_pre_inj], ...
                   out_cr3bp.departure.state.syn(:), opt);
state_pre_inj = S_pre(end, :);   % 1×6

%% ===== State after DSM1 =====
state_after_dsm1    = out_cr3bp.dsm1.state_pre.syn;          % 1×6
state_after_dsm1(4:6) = state_after_dsm1(4:6) + out_cr3bp.dsm1.dv.syn_adim;

%% ===== State at flyby (hyperbola periapsis, patched-conics approximation) =====
% Patched conics only give pre/post states at the Moon centre; reconstruct the
% flyby-hyperbola periapsis state as a first approximation of the true flyby
% (later refined in full ephemeris).
if isfield(c_const, 'muMoon')
    muMoon = c_const.muMoon;
else
    muMoon = c_const.G * c_const.mMoon;   % fallback [km^3/s^2]
end

state_flyby = flyby_periapsis_state(out_cr3bp.flyby.vinf_in(:), ...
                                    out_cr3bp.flyby.vinf_out(:), ...
                                    out_cr3bp.flyby.moon_state(:), ...
                                    out_cr3bp.flyby.epoch, muMoon, mu, c_const);

%% ===== State after DSM2 =====
state_after_dsm2    = out_cr3bp.dsm2.state_pre.syn;          % 1×6
state_after_dsm2(4:6) = state_after_dsm2(4:6) + out_cr3bp.dsm2.dv.syn_adim;

%% ===== Durations (non-dimensional time) =====
epoch_dep  = out_cr3bp.departure.epoch;
epoch_dsm1 = out_cr3bp.dsm1.epoch;
epoch_dsm2 = out_cr3bp.dsm2.epoch;
epoch_fly  = out_cr3bp.flyby.epoch;
epoch_com  = out_cr3bp.comet.epoch;

dur_inj_to_dsm1   = (epoch_dsm1 - epoch_dep)  / Tstar;
dur_dsm1_to_flyby = (epoch_fly  - epoch_dsm1) / Tstar;
dur_flyby_to_dsm2 = (epoch_dsm2 - epoch_fly)  / Tstar;
dur_dsm2_to_comet = (epoch_com  - epoch_dsm2) / Tstar;

%% ===== Comet arrival date =====
date_flyby_str = cspice_et2utc(epoch_com, 'ISOC', 3);   % comet, e.g. "2032-03-15T12:34:56.789"

%% ===== Write file =====
fid = fopen(filename, 'w');
if fid < 0
    error('write_python_inputs: cannot open the file "%s"', filename);
end

fprintf(fid, '# Trajectory data for Python – synodic adimensional CR3BP frame\n');
fprintf(fid, '# Generated by write_python_inputs.m on %s\n\n', ...
        datestr(now, 'yyyy-mm-dd HH:MM:SS'));

fprintf(fid, '# --- Halo parameters ---\n');
fprintf(fid, 'm = %.6f\n',       m);
fprintf(fid, 'T_halo = %.15f\n', T_halo);
fprintf(fid, '\n');

fprintf(fid, '# --- States (synodic adimensional: x y z vx vy vz) ---\n');
write_state(fid, 'state_after_injection', state_after_inj);
write_state(fid, 'state_pre_injection',   state_pre_inj);    % m*T_halo before inj
write_state(fid, 'state_after_dsm1',      state_after_dsm1);
write_state(fid, 'state_flyby',           state_flyby);       % hyperbola periapsis (approx)
write_state(fid, 'state_after_dsm2',      state_after_dsm2);
fprintf(fid, '\n');

fprintf(fid, '# --- Durations (adimensional time) ---\n');
fprintf(fid, 'dur_inj_to_dsm1 = %.15f\n',   dur_inj_to_dsm1);
fprintf(fid, 'dur_dsm1_to_flyby = %.15f\n', dur_dsm1_to_flyby);
fprintf(fid, 'dur_flyby_to_dsm2 = %.15f\n', dur_flyby_to_dsm2);
fprintf(fid, 'dur_dsm2_to_comet = %.15f\n', dur_dsm2_to_comet);
fprintf(fid, '\n');

fprintf(fid, '# --- Comet flyby date (UTC ISO) ---\n');
fprintf(fid, 'date_flyby = %s\n', date_flyby_str);

fclose(fid);
fprintf('Scritto: %s\n', filename);
end


%% =========================================================================
function write_state(fid, name, s)
% write_state  Write a single named 6-element state as a Python list line.
% Inputs: fid - open file id; name - variable name; s - 1x6 state.
    fprintf(fid, '%s = [%.15f, %.15f, %.15f, %.15f, %.15f, %.15f]\n', ...
            name, s(1), s(2), s(3), s(4), s(5), s(6));
end

