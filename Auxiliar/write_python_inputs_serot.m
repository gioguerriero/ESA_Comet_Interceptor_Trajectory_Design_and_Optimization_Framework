function write_python_inputs_serot(out_cr3bp, c_const, filename)
% write_python_inputs_serot  Like write_python_inputs but in the dimensional
% SEROT (Sun-Earth ROTating) frame.
%
% Same states as write_python_inputs, expressed in the Sun-Earth rotating frame
% (x toward Earth) but referred to the EARTH (Earth-centred) and DIMENSIONAL:
% positions in km, velocities in km/s. In this Earth-centred frame the Earth is
% at the origin and the Sun sits at -x. Durations are non-dimensional; the date
% is the comet arrival date.
%
% Inputs:
%   out_cr3bp - variables_organizer output
%   c_const   - constants struct with mu, Tstar and muMoon (or G+mMoon), rMoon
%   filename  - (optional) output file path (default 'python_inputs_serot.txt')
%
% Outputs:
%   (none) - writes the text file to disk

if nargin < 3
    filename = 'python_inputs_serot.txt';
end

mu    = c_const.mu;
Tstar = c_const.Tstar;

m      = 0.1;
T_halo = 3.083023392967281;   % [adim]

opt = odeset('AbsTol', 1e-13, 'RelTol', 1e-13);   % used only for the pre-injection back-propagation

%% ===== Event epochs =====================================================
epoch_dep     = out_cr3bp.departure.epoch;
epoch_pre_inj = epoch_dep - m * T_halo * Tstar;   % m*T_halo non-dim -> seconds
epoch_dsm1    = out_cr3bp.dsm1.epoch;
epoch_fly     = out_cr3bp.flyby.epoch;
epoch_dsm2    = out_cr3bp.dsm2.epoch;
epoch_com     = out_cr3bp.comet.epoch;

%% ===== Non-dimensional synodic states (identical to write_python_inputs) =====
% --- after the injection maneuver ---
sa_inj          = out_cr3bp.departure.state.syn;
sa_inj(4:6)     = sa_inj(4:6) + out_cr3bp.injection.syn_adim;

% --- m*T_halo before injection (back-propagated, no dv) ---
[~, S_pre] = ode45(@(t,S) CR3BP(t, S, mu), [0, -m*T_halo], ...
                   out_cr3bp.departure.state.syn(:), opt);
s_pre_inj  = S_pre(end, :);

% --- after DSM1 ---
sa_dsm1      = out_cr3bp.dsm1.state_pre.syn;
sa_dsm1(4:6) = sa_dsm1(4:6) + out_cr3bp.dsm1.dv.syn_adim;

% --- at flyby (hyperbola periapsis, patched-conics approximation) ---
if isfield(c_const, 'muMoon')
    muMoon = c_const.muMoon;
else
    muMoon = c_const.G * c_const.mMoon;
end
s_flyby = flyby_periapsis_state(out_cr3bp.flyby.vinf_in(:), ...
                                out_cr3bp.flyby.vinf_out(:), ...
                                out_cr3bp.flyby.moon_state(:), ...
                                epoch_fly, muMoon, mu, c_const);

% --- after DSM2 ---
sa_dsm2      = out_cr3bp.dsm2.state_pre.syn;
sa_dsm2(4:6) = sa_dsm2(4:6) + out_cr3bp.dsm2.dv.syn_adim;

%% ===== Convert non-dimensional synodic -> dimensional Earth-centred SEROT =====
state_after_injection = synodic_to_serot_earth(sa_inj,    epoch_dep,     mu);
state_pre_injection   = synodic_to_serot_earth(s_pre_inj, epoch_pre_inj, mu);
state_after_dsm1      = synodic_to_serot_earth(sa_dsm1,   epoch_dsm1,    mu);
state_flyby           = synodic_to_serot_earth(s_flyby,   epoch_fly,     mu);
state_after_dsm2      = synodic_to_serot_earth(sa_dsm2,   epoch_dsm2,    mu);

%% ===== Durations (non-dimensional time) ================================
dur_inj_to_dsm1   = (epoch_dsm1 - epoch_dep)  / Tstar;
dur_dsm1_to_flyby = (epoch_fly  - epoch_dsm1) / Tstar;
dur_flyby_to_dsm2 = (epoch_dsm2 - epoch_fly)  / Tstar;
dur_dsm2_to_comet = (epoch_com  - epoch_dsm2) / Tstar;

%% ===== Comet arrival date ==============================================
date_flyby_str = cspice_et2utc(epoch_com, 'ISOC', 3);

%% ===== Write file ======================================================
fid = fopen(filename, 'w');
if fid < 0
    error('write_python_inputs_serot: cannot open the file "%s"', filename);
end

fprintf(fid, '# Trajectory data for Python – SEROT (Sun-Earth rotating) frame\n');
fprintf(fid, '# Frame: Sun-centred rotating, x toward Earth; points EARTH-CENTRED, DIMENSIONAL.\n');
fprintf(fid, '# Units: position [km], velocity [km/s], durations [adimensional].\n');
fprintf(fid, '# Generated by write_python_inputs_serot.m on %s\n\n', ...
        datestr(now, 'yyyy-mm-dd HH:MM:SS'));

fprintf(fid, '# --- Halo parameters ---\n');
fprintf(fid, 'm = %.6f\n',       m);
fprintf(fid, 'T_halo = %.15f\n', T_halo);   % NB: T_halo stays non-dimensional (halo parameter)
fprintf(fid, '\n');

fprintf(fid, '# --- States (SEROT Earth-centred, dimensional: x y z [km], vx vy vz [km/s]) ---\n');
write_state_dim(fid, 'state_after_injection', state_after_injection);
write_state_dim(fid, 'state_pre_injection',   state_pre_injection);   % m*T_halo before inj
write_state_dim(fid, 'state_after_dsm1',      state_after_dsm1);
write_state_dim(fid, 'state_flyby',           state_flyby);            % hyperbola periapsis (approx)
write_state_dim(fid, 'state_after_dsm2',      state_after_dsm2);
fprintf(fid, '\n');

fprintf(fid, '# --- Durations (adimensional time) ---\n');
fprintf(fid, 'dur_inj_to_dsm1 = %.15f\n',   dur_inj_to_dsm1);
fprintf(fid, 'dur_dsm1_to_flyby = %.15f\n', dur_dsm1_to_flyby);
fprintf(fid, 'dur_flyby_to_dsm2 = %.15f\n', dur_flyby_to_dsm2);
fprintf(fid, 'dur_dsm2_to_comet = %.15f\n', dur_dsm2_to_comet);
fprintf(fid, '\n');

fprintf(fid, '# --- Comet flyby date (UTC ISO) ---\n');
fprintf(fid, 'date_flyby = %s\n', date_flyby_str);

fclose(fid);
fprintf('Scritto: %s\n', filename);
end


%% =========================================================================
function write_state_dim(fid, name, s)
% write_state_dim  Write a named 6-element dimensional state as a Python list.
    % Scientific format: dimensional values (km) can be large.
    fprintf(fid, '%s = [%.15e, %.15e, %.15e, %.15e, %.15e, %.15e]\n', ...
            name, s(1), s(2), s(3), s(4), s(5), s(6));
end


%% =========================================================================
%  HELPER: non-dimensional synodic -> dimensional Earth-centred SEROT [km, km/s]
%
%  1) synodic -> heliocentric J2000 (synodic2sun_J2000, exact, instantaneous
%     ephemerides at the epoch).
%  2) De-rotate about the Earth:
%       r = R'(r_J - r_E)
%       v = R'((v_J - v_E) - omega x (r_J - r_E))
%     with R = [e1 e2 e3], e1 = r_E/|r_E| (Sun->Earth), e3 = h/|h|, e2 = e3xe1,
%     omega = |h|/|r_E|^2.  No L scaling -> no Ldot term.
%% =========================================================================
function S_serot = synodic_to_serot_earth(S_syn, epoch, mu)
% synodic_to_serot_earth  Convert a non-dimensional synodic state to the
% dimensional Earth-centred SEROT frame. See the block above for the method.

    % 1) -> heliocentric J2000 (km, km/s)
    S_J2000 = synodic2sun_J2000(S_syn(:).', epoch, mu);   % 1×6

    % Earth ephemeris w.r.t. the Sun (ECLIPJ2000)
    ES = cspice_spkezr('EARTH', epoch, 'ECLIPJ2000', 'NONE', 'SUN');
    rE = ES(1:3);  vE = ES(4:6);

    L      = norm(rE);
    h      = cross(rE, vE);
    omega  = norm(h) / L^2;

    e1 = rE / L;
    e3 = h  / norm(h);
    e2 = cross(e3, e1);
    R  = [e1, e2, e3];

    omega_vec = e3 * omega;

    % 2) Earth-relative (inertial) state -> rotating frame
    r_rel = S_J2000(1:3).' - rE;
    v_rel = S_J2000(4:6).' - vE;

    r_serot = R' * r_rel;
    v_serot = R' * (v_rel - cross(omega_vec, r_rel));

    S_serot = [r_serot.' v_serot.'];
end
